public class Switch {
    public static void main(String[] args) {
        String option = "aaaa";
        switch (option) {
        case "1":
            System.out.println("Selected 1");
            break;
        case "2":
            System.out.println("Selected 2");
            break;
        case "3":
            System.out.println("Selected 3");
            break;
        default:
            System.out.printf("之杖，哪里有%s",option);
            }
        }
    
}
